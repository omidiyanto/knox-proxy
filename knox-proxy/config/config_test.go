func TestLoadFromEnv_AutoGeneratedRedirectURL(t *testing.T) {
	os.Setenv("OIDC_ISSUER_URL", "http://keycloak/realms/test")
	os.Setenv("OIDC_CLIENT_SECRET", "secret")
	os.Unsetenv("OIDC_REDIRECT_URL")
	os.Unsetenv("PROXY_PORT")
	defer func() {
		os.Unsetenv("OIDC_ISSUER_URL")
		os.Unsetenv("OIDC_CLIENT_SECRET")
	}()

	cfg, err := LoadFromEnv()
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	expected := "http://localhost:8443/auth/callback"
	if cfg.OIDCRedirectURL != expected {
		t.Errorf("expected auto-generated redirect URL %s, got %s", expected, cfg.OIDCRedirectURL)
	}
}

func TestGetEnv(t *testing.T) {
	os.Setenv("TEST_KEY", "value")
	defer os.Unsetenv("TEST_KEY")

	if v := getEnv("TEST_KEY", "default"); v != "value" {
		t.Errorf("expected value, got %s", v)
	}
	if v := getEnv("NONEXISTENT_KEY", "default"); v != "default" {
		t.Errorf("expected default, got %s", v)
	}
}

func TestGetEnvInt(t *testing.T) {
	os.Setenv("TEST_INT", "42")
	defer os.Unsetenv("TEST_INT")

	if v := getEnvInt("TEST_INT", 0); v != 42 {
		t.Errorf("expected 42, got %d", v)
	}
	if v := getEnvInt("NONEXISTENT_INT", 99); v != 99 {
		t.Errorf("expected 99, got %d", v)
	}

	os.Setenv("TEST_INT_BAD", "not-a-number")
	defer os.Unsetenv("TEST_INT_BAD")
	if v := getEnvInt("TEST_INT_BAD", 10); v != 10 {
		t.Errorf("expected default 10 for bad int, got %d", v)
	}
}

func TestGetEnvBool(t *testing.T) {
	os.Setenv("TEST_BOOL_TRUE", "true")
	os.Setenv("TEST_BOOL_FALSE", "false")
	defer func() {
		os.Unsetenv("TEST_BOOL_TRUE")
		os.Unsetenv("TEST_BOOL_FALSE")
	}()

	if v := getEnvBool("TEST_BOOL_TRUE", false); v != true {
		t.Error("expected true")
	}
	if v := getEnvBool("TEST_BOOL_FALSE", true); v != false {
		t.Error("expected false")
	}
	if v := getEnvBool("NONEXISTENT_BOOL", true); v != true {
		t.Error("expected default true")
	}
}
