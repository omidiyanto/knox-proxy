func TestLoadFromEnv_Defaults(t *testing.T) {
	os.Setenv("OIDC_ISSUER_URL", "http://keycloak/realms/test")
	os.Setenv("OIDC_CLIENT_SECRET", "secret")
	// Clear other values to test defaults
	os.Unsetenv("PROXY_PORT")
	os.Unsetenv("OIDC_CLIENT_ID")
	os.Unsetenv("KEYCLOAK_GROUP_PREFIX")
	os.Unsetenv("SESSION_MAX_AGE")
	os.Unsetenv("COOKIE_TTL")
	os.Unsetenv("N8N_TEAM_CREDENTIALS")
	os.Unsetenv("ENABLE_VAULT")
	defer func() {
		os.Unsetenv("OIDC_ISSUER_URL")
		os.Unsetenv("OIDC_CLIENT_SECRET")
	}()

	cfg, err := LoadFromEnv()
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	if cfg.ProxyPort != 8443 {
		t.Errorf("expected default port 8443, got %d", cfg.ProxyPort)
	}
	if cfg.OIDCClientID != "n8n-proxy" {
		t.Errorf("expected default client ID n8n-proxy, got %s", cfg.OIDCClientID)
	}
	if cfg.KeycloakGroupPrefix != "/n8n-prod-" {
		t.Errorf("expected default prefix /n8n-prod-, got %s", cfg.KeycloakGroupPrefix)
	}
	if cfg.SessionMaxAge != 3600 {
		t.Errorf("expected default session age 3600, got %d", cfg.SessionMaxAge)
	}
	if cfg.CookieTTL != 3600 {
		t.Errorf("expected default cookie TTL 3600, got %d", cfg.CookieTTL)
	}
	if cfg.EnableVault != false {
		t.Error("expected default EnableVault false")
	}
}

