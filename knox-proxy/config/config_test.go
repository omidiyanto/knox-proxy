func TestLoadFromEnv_AllSet(t *testing.T) {
	envVars := map[string]string{
		"PROXY_PORT":          "9000",
		"N8N_BACKEND_URL":     "http://nginx:80",
		"N8N_INTERNAL_URL":    "http://n8n:5678",
		"OIDC_ISSUER_URL":     "http://keycloak/realms/test",
		"OIDC_CLIENT_ID":      "test-client",
		"OIDC_CLIENT_SECRET":  "test-secret",
		"OIDC_REDIRECT_URL":   "http://localhost:9000/auth/callback",
		"SESSION_MAX_AGE":     "7200",
		"KEYCLOAK_GROUP_PREFIX": "/test-",
		"N8N_TEAM_CREDENTIALS": `{"team1":{"email":"t1@local","pass":"p1"}}`,
		"COOKIE_TTL":          "600",
		"DANGEROUS_NODES":     "n8n-nodes-base.code,n8n-nodes-base.ssh",
	}

	for k, v := range envVars {
		os.Setenv(k, v)
	}
	defer func() {
		for k := range envVars {
			os.Unsetenv(k)
		}
	}()

	cfg, err := LoadFromEnv()
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	if cfg.ProxyPort != 9000 {
		t.Errorf("expected port 9000, got %d", cfg.ProxyPort)
	}
	if cfg.N8NBackendURL != "http://nginx:80" {
		t.Errorf("unexpected backend URL: %s", cfg.N8NBackendURL)
	}
	if cfg.OIDCIssuerURL != "http://keycloak/realms/test" {
		t.Errorf("unexpected OIDC issuer: %s", cfg.OIDCIssuerURL)
	}
	if cfg.OIDCClientID != "test-client" {
		t.Errorf("unexpected client ID: %s", cfg.OIDCClientID)
	}
	if cfg.SessionMaxAge != 7200 {
		t.Errorf("expected session max age 7200, got %d", cfg.SessionMaxAge)
	}
	if cfg.KeycloakGroupPrefix != "/test-" {
		t.Errorf("unexpected group prefix: %s", cfg.KeycloakGroupPrefix)
	}
	if cfg.CookieTTL != 600 {
		t.Errorf("expected cookie TTL 600, got %d", cfg.CookieTTL)
	}
	if len(cfg.DangerousNodes) != 2 {
		t.Errorf("expected 2 dangerous nodes, got %d", len(cfg.DangerousNodes))
	}
	if len(cfg.TeamCredentials) != 1 {
		t.Errorf("expected 1 team credential, got %d", len(cfg.TeamCredentials))
	}
	cred, ok := cfg.TeamCredentials["team1"]
	if !ok {
		t.Fatal("expected team1 in credentials")
	}
	if cred.Email != "t1@local" {
		t.Errorf("expected t1@local, got %s", cred.Email)
	}
}

